TITLE 'Display even or odd'
.MODEL MEDIUM
.STACK 100H
.DATA
.CODE
MAIN PROC			
	MOV AH,1
	INT 21H
	CMP AL,'1'
	JE ODD_
	CMP AL,'3'
	JE ODD_
	CMP AL,'2'
	JE EVEN_
	CMP AL,'4'
	JE EVEN_
	JMP EXIT
	ODD_:
		MOV AL,'o'
	EVEN_:
		MOV AL,'e'
	JMP DISPLAY			; DOES NOT DIAPLAY O OR E.RATHER THEN <<
	DISPLAY:
		MOV AH,2
		INT 21H
	EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN